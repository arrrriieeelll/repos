
/**
 * Kelas Pesawat merepresentasikan data sebuah pesawat.
 */
class Pesawat {
    /** Kode pesawat (misal: GA123) */
    String kodePesawat;
    /** Nama pesawat (misal: Boeing 737) */
    String namaPesawat;
    /** Jumlah kursi yang tersedia */
    int jumlahKursi;

    /**
     * Konstruktor untuk membuat objek Pesawat.
     *
     * @param kode Kode pesawat
     * @param nama Nama pesawat
     * @param kursi Jumlah kursi
     */
    Pesawat(String kode, String nama, int kursi) {
        this.kodePesawat = kode;
        this.namaPesawat = nama;
        this.jumlahKursi = kursi;
    }
}

